    /**
     * Display the specified resource.
     */
    public function show(Post $post)
    {
        // Check if post is published (unless user is admin/author)
        if (!$post->is_published && (!Auth::check() || !Auth::user()->hasRole(['admin', 'author']))) {
            abort(404);
        }

        // Load relationships for the view
        $post->load(['category', 'user', 'tags']);

        return view('posts.show', compact('post'));
    }

